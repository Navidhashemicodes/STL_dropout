function z = reward(s)

FF = 0.1;
s = s(1:3,1);
scl = 10;
F   = zeros(1,15);
x     = cell(1,15);
x{1}  = [10; 2; 2]; 
x{2}  = [20; 4; 5];
x{3}  = [30; 7; 9];
x{4}  = [40;11;14];
x{5}  = [50;19;19];
x{6}  = [60;28;28];
x{7}  = [70;40;42];
x{8}  = [74;50;58];
x{9}  = [70;60;50];
x{10} = [60;70;42];
x{11} = [50;77;36];
x{12} = [40;82;32];
x{13} = [30;85;28];
x{14} = [20;87;25];
x{15} = [10;88;23];


z=0;
for i=1:15 
    F(i) = (s-FF*x{i})'*(s-FF*x{i});
    z = z + exp(  -F(i)  / (scl^2)  );
end

end